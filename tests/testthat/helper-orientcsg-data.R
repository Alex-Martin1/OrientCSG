expect_unit_vector <- function(x, tolerance = 1e-6) {
  expect_equal(sqrt(sum(x^2)), 1, tolerance = tolerance)
}

expect_orthogonal <- function(x, y, tolerance = 1e-6) {
  expect_equal(sum(x * y), 0, tolerance = tolerance)
}

expect_contains_fixed <- function(x, pattern) {
  expect_true(grepl(pattern, x, fixed = TRUE))
}
